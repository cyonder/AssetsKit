function toggleHeading(){
  var ele = document.getElementById("toggleHeading");
  var text = document.getElementById("displayHeading");

  if(ele.style.display === "block") {
    ele.style.display = "none";
    text.innerHTML = "show";
  }else{
    ele.style.display = "block";
    text.innerHTML = "hide";
  }
}

